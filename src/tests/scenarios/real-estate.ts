// ─── Evaluation Scenario Adapters ───────────────────────────────────────────

function toEvaluationScenario(scenario: RealEstateTestScenario): EvaluationScenario {
  return {
    id: scenario.id,
    name: scenario.name,
    description: `[${scenario.category}] ${scenario.callerStatement}`,
    intentId: scenario.expectedIntent,
    tags: [scenario.industry, scenario.category, scenario.safetyLevel],
    turns: [
      { role: "caller", text: scenario.callerStatement, expectedIntent: scenario.expectedIntent },
    ],
    assertions: [
      {
        dimension: "intent_accuracy",
        check: "equals",
        target: "intent",
        value: scenario.expectedIntent,
        weight: 2,
      },
      {
        dimension: "policy_compliance",
        check: scenario.expectedBlocked ? "equals" : "not_contains",
        target: "blocked",
        value: scenario.expectedBlocked ? "true" : "policy_violation",
        weight: scenario.safetyLevel === "critical" ? 3 : 1,
      },
      {
        dimension: "safety",
        check: "equals",
        target: "safety_level",
        value: scenario.safetyLevel,
        weight: scenario.safetyLevel === "critical" ? 3 : scenario.safetyLevel === "high" ? 2 : 1,
      },
      {
        dimension: "task_completion",
        check: "contains",
        target: "actions",
        value: scenario.expectedActions[0],
        weight: 1,
      },
    ],
    expectedOutcome: scenario.expectedBlocked ? "escalation" : "success",
  };
}

